
/*

When you use continue without a label, it terminates the current
iteration of the innermost enclosing while, do-while, or for statement
and continues execution of the loop with the next iteration.
In contrast to the break statement, continue does not terminate the execution
of the loop entirely. In a while loop, it jumps back to the condition.
In a for loop, it jumps to the increment-expression.

*/


continue;
continue label;
